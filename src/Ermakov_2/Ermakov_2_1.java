package Ermakov_2;

public class Ermakov_2_1 {
    public static void main(String[] args) {
        byte byteVar = 1;
        short shortVar = 2;
        int intVar = 3;
        long longVar = 4L;
        double doubleVar = 5.0;
        float floatVar = 6.0f;
        char charVar = 'A';
        boolean boolVar = true;

        System.out.println("byte = " + byteVar + "\n" +
                            "short = " + shortVar + "\n" +
                            "int = " + intVar + "\n" +
                            "long = " + longVar + "\n" +
                            "double = " + doubleVar + "\n" +
                            "float = " + floatVar + "\n" +
                            "char = " + charVar + "\n" +
                            "boolean = " + boolVar);
    }
}
